import streamlit as st
import pandas as pd
import os
import glob
from PIL import Image

# --- –ù–∞—Å—Ç—Ä–æ–π–∫–∏ —Å—Ç—Ä–∞–Ω–∏—Ü—ã ---
st.set_page_config(page_title="DENE Store", layout="wide")

# --- –û–±–ª–æ–∂–∫–∞ ---
banner_path = "data/images/banner.jpg"
if os.path.exists(banner_path):
    st.image(banner_path, use_container_width=True)
st.markdown("<h1 style='text-align:center;'>DENE Store. –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å!</h1>", unsafe_allow_html=True)

# --- –ó–∞–≥—Ä—É–∑–∫–∞ –≤—Å–µ—Ö –ª–∏—Å—Ç–æ–≤ Excel ---
excel_path = "data/catalog.xlsx"
sheets = pd.ExcelFile(excel_path).sheet_names

dfs = []
for sheet in sheets:
    df_temp = pd.read_excel(excel_path, sheet_name=sheet)
    df_temp["brand"] = sheet.strip()  # –∏–º—è –ª–∏—Å—Ç–∞ = –±—Ä–µ–Ω–¥
    dfs.append(df_temp)

df = pd.concat(dfs, ignore_index=True)
df.columns = df.columns.str.strip().str.lower()

# --- –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–ª—é—á–µ–≤—ã–µ –∫–æ–ª–æ–Ω–∫–∏ ---
required_cols = ["brand", "model", "gender", "color", "image", "size us", "price", "in stock"]
missing = [c for c in required_cols if c not in df.columns]
if missing:
    st.error(f"‚ùå –í —Ç–∞–±–ª–∏—Ü–µ –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç –∫–æ–ª–æ–Ω–æ–∫: {missing}")
    st.stop()

# --- –§–∏–ª—å—Ç—Ä—ã ---
col1, col2, col3, col4 = st.columns(4)

brands = sorted(df["brand"].dropna().unique().tolist())
models = sorted(df["model"].dropna().unique().tolist())
genders = sorted(df["gender"].dropna().unique().tolist())
sizes = sorted(df["size us"].dropna().unique().tolist())

brand_filter = col1.selectbox("–ë—Ä–µ–Ω–¥", ["–í—Å–µ"] + brands)
model_filter = col2.selectbox("–ú–æ–¥–µ–ª—å", ["–í—Å–µ"] + models)
gender_filter = col3.selectbox("–ü–æ–ª", ["–í—Å–µ"] + genders)
size_filter = col4.selectbox("–†–∞–∑–º–µ—Ä US", ["–í—Å–µ"] + sizes)

filtered_df = df.copy()
if brand_filter != "–í—Å–µ":
    filtered_df = filtered_df[filtered_df["brand"] == brand_filter]
if model_filter != "–í—Å–µ":
    filtered_df = filtered_df[filtered_df["model"] == model_filter]
if gender_filter != "–í—Å–µ":
    filtered_df = filtered_df[filtered_df["gender"] == gender_filter]
if size_filter != "–í—Å–µ":
    filtered_df = filtered_df[filtered_df["size us"] == size_filter]

# --- –ì—Ä—É–ø–ø–∏—Ä–æ–≤–∫–∞ –ø–æ –º–æ–¥–µ–ª–∏ –∏ —Ü–≤–µ—Ç—É ---
grouped = (
    filtered_df.groupby(["brand", "model", "color"], as_index=False)
    .agg({
        "image": "first",
        "price": "first",
        "in stock": "first",
        "size us": lambda x: sorted(x.dropna().unique().tolist()),
        "gender": "first"
    })
)

# --- –ü–æ–∏—Å–∫ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π ---
image_paths = glob.glob("data/images/**/*.*", recursive=True)
image_map = {os.path.splitext(os.path.basename(p))[0]: p for p in image_paths}

# --- –í—ã–≤–æ–¥ –∫–∞—Ä—Ç–æ—á–µ–∫ ---
st.markdown("### –ö–∞—Ç–∞–ª–æ–≥ —Ç–æ–≤–∞—Ä–æ–≤")

if grouped.empty:
    st.warning("üòï –ù–µ—Ç —Ç–æ–≤–∞—Ä–æ–≤ –ø–æ –∑–∞–¥–∞–Ω–Ω—ã–º —Ñ–∏–ª—å—Ç—Ä–∞–º.")
else:
    cols = st.columns(4)
    for idx, (_, row) in enumerate(grouped.iterrows()):
        with cols[idx % 4]:
            image_names = str(row["image"]).split()
            found = None
            for name in image_names:
                if name in image_map:
                    found = image_map[name]
                    break
            
            if found:
                st.image(found, use_container_width=True)
            else:
                st.image("data/images/no_image.jpg", use_container_width=True)

            st.markdown(f"**{row['brand']} {row['model']} ({row['color']})**")
            st.markdown(f"–ü–æ–ª: {row['gender']}")
            st.markdown(f"–†–∞–∑–º–µ—Ä—ã: {', '.join(map(str, row['size us']))}")
            st.markdown(f"–¶–µ–Ω–∞: {int(row['price'])} ‚Ç∏")

            color = "green" if str(row["in stock"]).strip().lower() == "yes" else "red"
            st.markdown(f"<p style='font-size:13px; color:{color};'>–í –Ω–∞–ª–∏—á–∏–∏: {row['in stock']}</p>", unsafe_allow_html=True)
